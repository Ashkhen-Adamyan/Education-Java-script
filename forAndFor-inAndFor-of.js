// for in пример

let fruits = ["яблоко", "банан", "апельсин", "ананас", "дыня"];

console.log("сегодня я съел:");

for (let fruit of fruits) {
  console.log(fruit);
}
/* сегодня я съел:
яблоко
банан
апельсин
ананас
дыня */

// for of пример

let rating = ["Катя", "Петя", "Вася", "Оля", "Ваня"];

console.log("Рейтинг студентов:");

for (let i in rating) {
  console.log(`${parseInt(i) + 1} место: ${rating[i]}`);
}

/* Рейтинг студентов:
1 место: Катя
2 место: Петя
3 место: Вася
4 место: Оля
5 место: Ваня */
